    @Override
    public void compra(Venda venda) {
        this.setVisible(true);
        //Seta as informações da compra na janela
        this.lbl_idVenda.setText(Integer.toString(venda.getId_venda()));
        this.lbl_totalItens.setText(Integer.toString(venda.getTotalItens()));
        this.lbl_totalCompra.setText("R$" + String.format("%,.2f", venda.getTotal()));
        //Muda título da janela para o id da venda
        this.setTitle("Venda Nº" + venda.getId_venda());
    }

